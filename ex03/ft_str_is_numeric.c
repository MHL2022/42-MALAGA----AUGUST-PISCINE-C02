#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if (((*str <  48) || (*str > 57)) && (*str != ' '))
			return (0);
		a++;
	}
	return (1);
}
